
/*Setting up a function with arguments*/
function compoundWords (wordOne, wordTwo) {
  console.log ("Compound Word: " + (wordOne + wordTwo));
}

/*Choosing words for the compoundWords function*/
compoundWords ("sun", "flower")

/*Return a concatenated string*/
function compoundWordsandreturn (wordOne, wordTwo) {
  return wordOne + wordTwo;
}

console.log (compoundWordsandreturn ("snow", "flake"))
